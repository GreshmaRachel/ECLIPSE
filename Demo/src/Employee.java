
public class Employee {
	private int empid;
	private int empname;
	public Employee(int empid, int empname) {
		super();
		this.empid = empid;
		this.empname = empname;
	}
	public int getEmpid() {
		return empid;
	}
	public void setEmpid(int empid) {
		this.empid = empid;
	}
	public int getEmpname() {
		return empname;
	}
	public void setEmpname(int empname) {
		this.empname = empname;
	}

}
